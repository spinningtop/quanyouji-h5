  function inferTimeBucket(minutes) {
    if (minutes <= 90) return "1h";
    if (minutes <= 190) return "3h";
    if (minutes <= 270) return "halfday";
    return "day";
  }

  function normalizeScene(tag) {
    if (tag.includes("亲子") || tag.includes("老人")) return "family";
    if (tag.includes("拍照")) return "photo";
    if (tag.includes("文化") || tag.includes("学生")) return "culture";
    if (tag.includes("夜")) return "night";
    if (tag.includes("小众") || tag.includes("本地")) return "hidden";
    if (tag.includes("经典") || tag.includes("第一次")) return "classic";
    return "";
  }

  function inferScenes(tags) {
    const scenes = tags.map(normalizeScene).filter(Boolean);
    return scenes.length ? Array.from(new Set(scenes)) : ["hidden"];
  }

  function inferLevel(springData, routeIds) {
    if (["baotu", "heihu", "wulongtan"].includes(springData.id)) return "S";
    if (springData.detailLevel === "featured" && routeIds.length >= 2) return "A";
    if (springData.detailLevel === "featured") return "B";
    if (springData.group !== "其他") return "B";
    return "C";
  }

  function mapTrust(springData) {
    if (springData.level === "S" || springData.vrPanorama?.trustLevel === "exact") {
      return { level: "exact", label: "精准泉眼", note: "可直接导航" };
    }
    if (/公园内|景区内|寺内/.test(springData.location)) {
      return { level: "entrance", label: "景区入口", note: "入园后按提示找泉" };
    }
    return { level: "area", label: "片区中心", note: "到附近后再确认" };
  }

  function inferNearbySpringIds(springId, routeIds) {
    const nearby = [];
    routeIds.forEach((routeId) => {
      const routeData = routes.find((item) => item.id === routeId);
      if (!routeData) return;
      const index = routeData.stopIds.indexOf(springId);
      [routeData.stopIds[index - 1], routeData.stopIds[index + 1]].forEach((id) => {
        if (id && id !== springId && !nearby.includes(id)) nearby.push(id);
      });
    });
    return nearby.slice(0, 4);
  }

  function attachImage(springData, name) {
    const localImages = imageAssets[springData.id] || [];
    if (localImages.length) {
      return {
        ...springData,
        imageStatus: "selected",
        coverImage: localImages[0],
        galleryImages: localImages,
        verificationStatus: "泉游记精选资料"
      };
    }
    const matched = verifiedImages[name];
    if (!matched) return {
      ...springData,
      verificationStatus: "泉游记精选资料"
    };
    return {
      ...springData,
      imageStatus: "verified",
      coverImage: matched,
      galleryImages: [
        matched,
        routeImage(matched.src, `${name}现场细节`, matched.source)
      ]
    };
  }

  function renderDataNextStepTip(springData) {
    const routeData = routes.find((item) => item.stopIds.includes(springData.id));
    if (!routeData) return "可以回到图鉴继续看同片区泉点。";
    const index = routeData.stopIds.indexOf(springData.id);
    const nextId = routeData.stopIds[index + 1];
    const next = springs.find((item) => item.id === nextId);
    return next ? `下一站可去${next.name}，继续沿着${routeData.shortName}走。` : "这是当前路线的收尾点，适合回到我的行程查看完成记录。";
  }

  function enrichGalleryCaptions(springData) {
    const images = springData.galleryImages && springData.galleryImages.length ? springData.galleryImages : [springData.coverImage].filter(Boolean);
    const base = images.slice(0, 5).map((item, index) => ({
      ...item,
      caption: buildGalleryCaption(springData, item.caption, index)
    }));
    while (base.length < 3 && springData.coverImage) {
      base.push({
        ...springData.coverImage,
        caption: buildGalleryCaption(springData, springData.coverImage.caption || springData.name, base.length)
      });
    }
    return base;
  }

